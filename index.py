import math

raio = 10
base = 5
expoente = 3

#math.pi: mostra o valor do Pi
perimetro = 2 * math.pi * raio

#math.pow(): mostra o valor da base elevada ao expoente
potencia = math.pow(base, expoente)

#math.sqrt: mostra a raiz quadrada de um numero
raiz_quadrada = math.sqrt(144)

print(f"Perímetro: {perimetro:.2f}")
print(f"Potência: {potencia}")
print(f"Raiz Quadrada: {raiz_quadrada}")